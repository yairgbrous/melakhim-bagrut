export function getCharacterById(id) {
  return characters.find((c) => c.id === id) || null;
}

export function searchCharacters(query) {
  const q = (query || "").trim();
  if (!q) return characters;
  return characters.filter((c) => {
    const haystack = [
      c.name,
      c.nameEn,
      c.era,
      c.kingdom,
      ...(c.tags || []),
      ...(c.traits || []),
    ]
      .join(" ")
      .toLowerCase();
    return haystack.includes(q.toLowerCase());
  });
}

